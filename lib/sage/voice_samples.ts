/** 返回 N 条 sample，markdown 块格式（用于注入 prompt） */
export function formatVoiceSamples(samples: VoiceSample[], n = 12): string {
  return samples.slice(0, n).map((s, i) =>
    `### 样本 ${i + 1} [${s.date} 赞${s.likes}, ${s.text.length}字]\n${s.text}`
  ).join("\n\n");
}
